class View
  # print the grid, given an array of strings
  def print_sudoku(grid)
    grid.each.with_index do |row, index|
      puts "-"*35 if index == 0 or index == 3 or index == 6
      puts row
      puts "-"*35 if index == grid.length-1
    end
  end

end


